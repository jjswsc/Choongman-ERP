export function useT(lang: string) {
  const key = (lang in i18n ? lang : 'ko') as keyof typeof i18n
  const dict = i18n[key] || i18n.ko
  return (k: I18nKeys) => (dict as Record<I18nKeys, string>)[k] ?? (i18n.ko as Record<I18nKeys, string>)[k] ?? k
}
