-- ============================================================
-- 직원시간표(schedules) 날짜 1일 보정
-- ============================================================
-- 원인: 마이그레이션 시 시트 Date 셀을 toISOString()으로 변환하면서
--       UTC 기준으로 잘려 태국(Asia/Bangkok) 기준보다 하루 앞으로 저장됨.
-- 적용: Supabase SQL Editor에서 이 스크립트를 한 번 실행하세요.
--       (이미 보정된 데이터에 다시 실행하면 날짜가 또 하루씩 밀리므로 1회만 실행)
-- ============================================================

UPDATE schedules
SET schedule_date = schedule_date + INTERVAL '1 day';

